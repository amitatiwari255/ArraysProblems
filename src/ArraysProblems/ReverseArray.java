package ArraysProblems;

public class ReverseArray {
    public static void main (String[] args){
    int[] numArray = {23,45,67,89,43,65};
    for (int i = 0 ; i < numArray.length ; i++){
        System.out.print(  "  " +numArray[i]);
    }
    for (int i = 7 ; i > 0 ; i --) {
        System.out.println("  " +i);
}
    }
}
